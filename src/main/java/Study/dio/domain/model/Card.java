package Study.dio.domain.model;

public class Card {
	private String number;
	private Double limit;
	
	public Card() {
		super();
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public Double getLimit() {
		return limit;
	}

	public void setLimit(Double limit) {
		this.limit = limit;
	}
	
	
	

}
